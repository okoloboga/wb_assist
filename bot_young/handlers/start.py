                "–î–ª—è –Ω–∞—á–∞–ª–∞ —Ä–∞–±–æ—Ç—ã –ø–æ–¥–∫–ª—é—á–∏—Ç–µ –∫–∞–±–∏–Ω–µ—Ç –≤ –æ—Å–Ω–æ–≤–Ω–æ–º –±–æ—Ç–µ:",
